package domain.tech.dto;

public class TechInfo {
	private String techName;
	private String techIdx;
	private String famous;
	private String frontend;
	private String backend;
	private String mobile;
	private String etc;
	private String allTech;
	
	public String getTechName() {
		return techName;
	}
	public void setTechName(String techName) {
		this.techName = techName;
	}
	public String getTechIdx() {
		return techIdx;
	}
	public void setTechIdx(String techIdx) {
		this.techIdx = techIdx;
	}
	public String getFamous() {
		return famous;
	}
	public void setFamous(String famous) {
		this.famous = famous;
	}
	public String getFrontend() {
		return frontend;
	}
	public void setFrontend(String frontend) {
		this.frontend = frontend;
	}
	public String getBackend() {
		return backend;
	}
	public void setBackend(String backend) {
		this.backend = backend;
	}
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	public String getEtc() {
		return etc;
	}
	public void setEtc(String etc) {
		this.etc = etc;
	}
	public String getAllTech() {
		return allTech;
	}
	public void setAllTech(String allTech) {
		this.allTech = allTech;
	}
	
	
}
